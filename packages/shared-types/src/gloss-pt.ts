/** Rejeita metadados STEP importados por engano como glossPt (ex.: #01, #03»04:G3076). */
export function isStoredGlossPtValid(value: string): boolean {
  const v = value.trim();
  if (!v) return false;
  if (/^#\d+/.test(v)) return false;
  if (/»/.test(v)) return false;
  if (/^[GH]\d{1,5}[A-Za-z]?$/.test(v)) return false;
  if (/^[:#»\dGHA-Za-z.\-_]+$/.test(v) && !/[áàâãéêíóôõúçÁÀÂÃÉÊÍÓÔÕÚÇ]/.test(v)) return false;
  return true;
}

function normalizeSegmentKey(segment: string): string {
  return segment.trim().toLowerCase();
}

/** Extrai o primeiro termo traduzível do léxico Strong em português. */
export function primaryLexiconGloss(shortDefinition: string): string | undefined {
  const cleaned = shortDefinition.replace(/\s+/g, " ").trim();
  if (!cleaned) return undefined;

  const kjv = cleaned.match(/\)\s*:?\s*--?\s*([^,;.(]+)/);
  if (kjv?.[1]) {
    const word = kjv[1].replace(/^X\s+/, "").trim();
    if (word && !/^veja\s/i.test(word)) return word;
  }

  const colon = cleaned.match(/:\s*-+\s*([^,;.(]+)/);
  if (colon?.[1]) {
    const word = colon[1].replace(/^X\s+/, "").trim();
    if (word && !/^veja\s/i.test(word)) return word;
  }

  return undefined;
}

function translateSegment(segment: string, lexiconGloss?: string): string {
  const trimmed = segment.trim();
  if (!trimmed) return segment;

  const key = normalizeSegmentKey(trimmed);
  const mapped = INTERLINEAR_EN_TO_PT[key];
  if (mapped) return preserveSegmentCasing(trimmed, mapped);

  if (trimmed.includes(" ")) {
    const words = trimmed.split(/\s+/);
    const translated = words.map((word) => {
      const bare = word.replace(/[,.;:!?]+$/, "");
      const suffix = word.slice(bare.length);
      const wordKey = normalizeSegmentKey(bare);
      const mapped = INTERLINEAR_EN_TO_PT[wordKey];
      return mapped ? `${mapped}${suffix}` : word;
    });
    if (translated.some((word, index) => word !== words[index])) {
      return translated.join(" ");
    }
  }

  if (lexiconGloss && !trimmed.includes("/") && !trimmed.startsWith("<") && !trimmed.startsWith("[")) {
    return lexiconGloss;
  }

  if (/^[A-Z]/.test(trimmed) && !mapped) return trimmed;

  return trimmed;
}

function preserveSegmentCasing(original: string, translated: string): string {
  if (original.startsWith("[") && !translated.startsWith("[")) return `[${translated}]`;
  if (original.startsWith("<") && !translated.startsWith("<")) return `<${translated}>`;
  if (original[0] === original[0]?.toUpperCase() && original[0] !== original[0]?.toLowerCase()) {
    return translated.charAt(0).toUpperCase() + translated.slice(1);
  }
  return translated;
}

/** Deriva gloss PT a partir do gloss EN, preservando barras e marcadores gramaticais. */
export function deriveGlossPtFromEn(
  glossEn: string,
  lexiconShortDefinition?: string
): string | undefined {
  const en = glossEn.trim();
  if (!en) return undefined;

  const lexiconGloss = lexiconShortDefinition
    ? primaryLexiconGloss(lexiconShortDefinition)
    : undefined;

  if (en.includes("/")) {
    const parts = en.split("/");
    const translated = parts.map((part, index) => {
      const leading = part.match(/^\s*/)?.[0] ?? "";
      const core = part.trim();
      const pt = translateSegment(core, index === parts.length - 1 ? lexiconGloss : undefined);
      return `${leading}${pt}`;
    });
    return translated.join("/");
  }

  const direct = INTERLINEAR_EN_TO_PT[normalizeSegmentKey(en)];
  if (direct) return preserveSegmentCasing(en, direct);

  if (lexiconGloss) return lexiconGloss;

  if (/^[A-Z]/.test(en)) return en;

  return undefined;
}

export function resolveTokenGlossPt(
  glossEn: string | null | undefined,
  glossPt: string | null | undefined,
  lexiconShortDefinition?: string
): string | undefined {
  const stored = glossPt?.trim();
  if (stored && isStoredGlossPtValid(stored)) return stored;

  const en = glossEn?.trim();
  if (!en) return undefined;

  return deriveGlossPtFromEn(en, lexiconShortDefinition);
}
